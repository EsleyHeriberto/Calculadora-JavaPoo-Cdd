package Metodos;

public class SomarMetodos {

	int somar (int a, int b) {
		return a + b;
	}
	int somar (int a, int b , int c ) {
		return a + b + c;
	}

	int diminuir (int a, int b) {
		return a - b;
	}
	int diminuir (int a, int b , int c ) {
		return a - b - c;
	}
	
	int multiplicacao (int a, int b) {
		return a * b;
	}
	int multiplicacao (int a, int b , int c ) {
		return (a * b) * c;
	}
	
	Double divisao (Double a, Double b) {
		return a / b;
	}
	Double divisao (Double a, Double b , Double c ) {
		return (a / b) / c;
	}
}
